const CACHE_NAME = 'pokemon-builder-v1.2.1';
const urlsToCache = [
  '/Pokemon-Savage-Dungeon/',
  '/Pokemon-Savage-Dungeon/index.html',
  '/Pokemon-Savage-Dungeon/pokemon.csv',
  '/Pokemon-Savage-Dungeon/item.csv',
  '/Pokemon-Savage-Dungeon/favicon.ico',
  '/Pokemon-Savage-Dungeon/manifest.json'
];

// Install Service Worker
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        return cache.addAll(urlsToCache).catch(err => {
          console.log('Cache addAll error:', err);
          // Don't fail on cache errors, allow offline functionality anyway
        });
      })
  );
  self.skipWaiting();
});

// Activate Service Worker
self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheName !== CACHE_NAME) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
  self.clients.claim();
});

// Fetch Event
self.addEventListener('fetch', event => {
  // Skip non-GET requests
  if (event.request.method !== 'GET') {
    return;
  }

  event.respondWith(
    caches.match(event.request)
      .then(response => {
        // Return from cache if available
        if (response) {
          return response;
        }

        return fetch(event.request).then(response => {
          // Don't cache non-successful responses
          if (!response || response.status !== 200 || response.type !== 'basic') {
            return response;
          }

          // Clone the response
          const responseToCache = response.clone();
          caches.open(CACHE_NAME).then(cache => {
            cache.put(event.request, responseToCache);
          });

          return response;
        });
      })
      .catch(() => {
        // Return offline page or cached resource
        return caches.match('/Pokemon-Savage-Dungeon/index.html');
      })
  );
});
